    public void getUserAccount(){
        try {
            // Read client account data
            File clientData = new File("clientList.txt");
            Scanner myReader = new Scanner(clientData);
            while (myReader.hasNextLine()) {
                String data = myReader.nextLine();
                String[] temp = data.split("#");
                clientList.add(new Client(temp[0], temp[1], temp[2], temp[3], temp[4]));
            }
            myReader.close();
            // Read staff account data
            File staffData = new File("staffList.txt");
            Scanner myReader2 = new Scanner(staffData);
            while (myReader2.hasNextLine()) {
                String data = myReader2.nextLine();
                String[] temp = data.split("#");
                staffList.add(new Staff(temp[0], temp[1], temp[2], temp[3], temp[4], temp[5]));
            }
            myReader2.close();
        } catch (FileNotFoundException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
    }
    
