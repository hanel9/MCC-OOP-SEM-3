    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean isFound = false;
        int i;
        String userType = "";
        String valueOfUsername = txtUsername.getText();
        String valueOfPassword = String.valueOf(txtPassword.getPassword());
        System.out.println(valueOfUsername);
        System.out.println(valueOfPassword);
        // Search for client account
        for(i = 0 ; i < clientList.size()-1 ; i++){
            if(clientList.get(i).getUsername().equals(valueOfUsername) && clientList.get(i).getPassword().equals(valueOfPassword)){
                isFound = true;
                userType = "CLIENT";
                break;
            }
        }
        // Search for staff account
        if(!isFound){
            for(i = 0 ; i < staffList.size()-1 ; i++){
                if(staffList.get(i).getUsername().equals(valueOfUsername) && staffList.get(i).getPassword().equals(valueOfPassword)){
                    isFound = true;
                    userType = "STAFF";
                    break;
                }
            }
        }
        if(isFound){
            try{
                File file = new File("activeUser.txt");
                if(file.createNewFile()){
                    System.out.println("File created!");
                }
                else{
                    System.out.println("File already exist! Overwriting existing file");
                }
                try (FileWriter writer = new FileWriter("activeUser.txt")) {
                    if(!userType.isEmpty() && userType.equals("CLIENT")){
                        String temp = clientList.get(i).getUserId()
                                + "#" + clientList.get(i).getUserType()
                                + "#" + clientList.get(i).getName()
                                + "#" + clientList.get(i).getUsername()
                                + "#" + clientList.get(i).getPassword()
                                + "\n";
                        writer.write(temp);
                        // Open main page
                        clientMainPageExchangeUI mp = new clientMainPageExchangeUI();
                        mp.show();
                    }
                    else if(!userType.isEmpty() && userType.equals("STAFF")){
                        String temp = staffList.get(i).getUserId()
                                + "#" + staffList.get(i).getUserType()
                                + "#" + staffList.get(i).getOfficeBranch()
                                + "#" + staffList.get(i).getName()
                                + "#" + staffList.get(i).getUsername()
                                + "#" + staffList.get(i).getPassword()
                                + "\n";
                        writer.write(temp);
                        staffDashboardPageUI dp = new staffDashboardPageUI();
                        dp.show();
                    }
                    this.dispose();
                }
            }catch(IOException e){
                System.out.println("An error occurred");
            }
        }else{
            // If no account found, change color
            txtUsername.setBackground(Color.decode("#FFCCCC"));
            txtPassword.setBackground(Color.decode("#FFCCCC"));
        }
    }//GEN-LAST:event_btnLoginActionPerformed

